# Números Inteiros
numeroInteiro = 4
print("Número inteiro = ", numeroInteiro)

# Número Real com ponto flutuante
numeroReal = 3.14
print("Real com ponto flutuante = ", numeroReal)

# type() - Mostra o tipo de variavel do resultado 
print("Tipo da variavel Inteiro = ", type(numeroInteiro))
print("Tipo da variavel Real = ", type(numeroReal))

# Soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# Subtracao -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# Multiplicacao *
multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

# Divisao / 
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo da variavel do resultado da divisao = ", type(divisao))

# divisao // - Divisão com o resultado sempre com número inteiro
divisao = 5 // 2
print("5 // 2 = ", divisao)
print("Tipo da variavel do resultado da divisao = ", type(divisao))

# int() - Transforma o resultado em um número inteiro
print("Valor em inteiro = ", int(divisao))

# float() - Transforma o resultado em número float
print("Valor em float = ", float(divisao))

# Modulo % - Mostro o restante da divisão
modulo = 5 % 2
print("Modulo = ", modulo)

