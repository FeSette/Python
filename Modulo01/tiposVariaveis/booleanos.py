# Booleano tem apenas 2 condições
# TRUE e FALSE

## Condicionais
if True:
    print("Bloco IF será executado!")
else:
    print("Bloco ELSE não será executado!")

## Operadores lógicos: 
# AND (Será executado somente se as duas condições forem verdadeiras)
if True and True:
    print("Bloco será executado!")

if True and False:
    print("Bloco náo será executado!")

if False and False:
    print("Bloco não será executado!")

#OR (Será executado se uma condção for verdadeira)
if True or False:
    print ("Bloco será executado!")

if False or False:
    print ("Bloco não será executado!")

if True or True:
    print ("Bloco será executado!")


          